public class program01 {
	
	public static void main(String[] args) {
		cal(10,20);
		System.out.println("#######");
		cal(20,30);
		System.out.println("@@@@@@@@");
		cal(30,40);
//multiple numbers having same operations
	}
public static void cal(int a,int b)
{
	System.out.println(a+b);
	System.out.println(a-b);
	System.out.println(a*b);
}
}
